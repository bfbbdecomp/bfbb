    sum->TotalAudioDecompTime = bnk->timeadecomp;
    sum->TotalVideoDecompTime = bnk->timevdecomp;
    sum->TotalBlitTime = bnk->timeblit;
    sum->HighestMemAmount += bnk->totalmem;
    sum->TotalIOMemory = bnk->iosize;
