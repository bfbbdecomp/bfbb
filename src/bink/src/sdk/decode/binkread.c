static void GotoFrame(HBINK bnk, u32 frame)
{
    u32 frame_offset;

    if (frame != 0)
    {
        frame = frame - 1;
    }
    bnk->skipped_this_frame = 0;
    if (bnk->playingtracks != 0)
    {
        checksound(bnk);
    }
    bnk->LastFrameNum = bnk->FrameNum;
    if (frame == 0)
    {
        if (bnk->soundon != 0)
        {
            BinkSetSoundOnOff(bnk, BINK_SOUND_OFF);
            BinkSetSoundOnOff(bnk, BINK_SOUND_ON);
        }
        bnk->startsynctime = 0;
        bnk->big_sound_skip_adj = 0;
    }
    if (bnk->preloadptr != 0)
    {
        bnk->compframe = bnk->preloadptr + (BINK_FRAME_OFFSET(bnk->frameoffsets[frame]) -
                                            BINK_FRAME_OFFSET(*bnk->frameoffsets));
    }
    else
    {
        frame_offset = BINK_FRAME_OFFSET(bnk->frameoffsets[frame]);
        (bnk->bio.ReadFrame)(&bnk->bio, frame, frame_offset, bnk->compframe,
                             BINK_FRAME_OFFSET(bnk->frameoffsets[frame + 1]) -
                                 frame_offset);
    }
    if (bnk->playingtracks != 0)
    {
        checksound(bnk);
    }
    bnk->FrameNum = frame + 1;
}

