HBINKTRACK BinkOpenTrack(HBINK bnk, u32 trackindex)
{
    u32 tracktype;
    u32 open_tracktype;
    HBINKAUDIODECOMP sndcomp;
    HBINKTRACK track;

    if (bnk == 0) {
        return 0;
    }
    if (trackindex >= bnk->NumTracks) {
        return 0;
    }

    tracktype = bnk->tracktypes[trackindex];
    if (!BINKTRACKISOPENABLE(tracktype)) {
        return 0;
    }

    open_tracktype = bnk->tracktypes[trackindex];
    sndcomp = BinkAudioDecompressOpen(BINKTRACKFREQ(open_tracktype),
                                      BINKTRACKCHANNELS(open_tracktype),
                                      BINKTRACKDECOMPFLAGS(open_tracktype));
    if (sndcomp == 0) {
        return 0;
    }

    track = bpopmalloc(bnk, sizeof(*track));
    if (track == 0) {
        BinkAudioDecompressClose(sndcomp);
        goto fail;
    }

    memset(track, 0, sizeof(*track));
    track->sndcomp = (UINTa)sndcomp;
    track->bink = bnk;
    track->Frequency = BINKTRACKFREQ(bnk->tracktypes[trackindex]);
    track->Bits = BINKTRACKBITS(bnk->tracktypes[trackindex]);
    track->Channels = BINKTRACKCHANNELS(bnk->tracktypes[trackindex]);
    track->MaxSize = BINK_TRACK_BUFFER_BYTES(bnk->tracksizes[trackindex]);
    if (track->Bits == BINK_SOUND_BITS_8) {
        track->MaxSize >>= 1;
    }
    track->trackindex = trackindex;

    return track;

fail:
    return 0;
}

