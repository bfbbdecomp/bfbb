    read_time = bnk->bio.TotalTime;
    sum->TotalReadSpeed = mult64anddiv(bytes_read, BINK_MILLISECONDS_PER_SECOND, read_time + 1);
    sum->TotalReadTime = bnk->bio.ForegroundTime;
    sum->TotalIdleReadTime = bnk->bio.IdleTime;
    sum->TotalBackReadTime = bnk->bio.ThreadTime;

    data_size = bnk->Size - BINK_FRAME_OFFSET(*bnk->frameoffsets);
    sum->AverageDataRate = mult64anddiv(data_size, bnk->fileframerate, bnk->fileframeratediv * bnk->Frames);
    sum->AverageFrameSize = data_size / bnk->Frames;
